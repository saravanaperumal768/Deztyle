    $(document).ready(function () {
        // Listen for the "Add to Cart" button click
        $(document).on('click', '.product-cart-btn', function () {
            let productId = $(this).data('product-id');
            
            // Make AJAX POST request to add product to cart
            $.ajax({
                url: "{{ route('cart.add') }}",
                type: "POST",
                data: {
                    product_id: productId,
                    _token: "{{ csrf_token() }}"
                },
                success: function (response) {
                    if (response.success) {
                        // Update the cart count dynamically
                        $('.shop-cart span').text(response.cartCount);

                        // Optionally show a success message
                        alert(response.message);

                        // Update the cart dropdown dynamically
                        updateCartDropdown(response.cart, response.total);
                    }
                },
                error: function (xhr) {
                    alert('Something went wrong!');
                }
            });
        });
    });

    // Function to update the cart dropdown with the new cart items
    function updateCartDropdown(cart, total) {
        // Update the cart items list
        let cartList = '';
        $.each(cart, function (id, item) {
            cartList += `
                <li>
                    <div class="dropdown-cart-item">
                        <div class="cart-img">
                            <a href="#"><img src="{{ asset('${item.image}') }}" alt="#"></a>
                        </div>
                        <div class="cart-info">
                            <h4><a href="#">${item.name}</a></h4>
                            <p class="cart-qty">${item.quantity}x - 
                                <span class="cart-amount">$${item.price * item.quantity}</span>
                            </p>
                        </div>
                        <a href="#" class="cart-remove" title="Remove this item"><i class="far fa-times-circle"></i></a>
                    </div>
                </li>
            `;
        });

        // Update the cart dropdown with the new items
        $('.dropdown-cart-list').html(cartList);

        // Update the total price
        $('.total-amount').text('$' + total);
    }
</script>





